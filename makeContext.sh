#!/bin/bash

# Название выходного файла
OUTPUT_FILE="all_files_combined.txt"

# Очищаем файл, если он уже существует
> "$OUTPUT_FILE"

echo "Сборка файлов началась..."

# Находим все файлы в текущей директории и поддиректориях
# -type f : только файлы
# -not -path '*/.*' : игнорировать скрытые папки и файлы (например, .git, .target)
find . -type f -not -path '*/.*' | while read -r file; do
    
    # Проверяем, не является ли файл бинарным (чтобы не испортить текст)
    if grep -qI . "$file"; then
        echo "Добавляю: $file"
        
        # Записываем путь и разделитель
        echo "================================================================" >> "$OUTPUT_FILE"
        echo "FILE: $(realpath "$file")" >> "$OUTPUT_FILE"
        echo "================================================================" >> "$OUTPUT_FILE"
        
        # Добавляем содержимое файла
        cat "$file" >> "$OUTPUT_FILE"
        
        # Добавляем пустую строку в конце для чистоты
        echo -e "\n" >> "$OUTPUT_FILE"
    fi
done

echo "Готово! Все данные собраны в: $OUTPUT_FILE"
