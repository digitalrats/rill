//! Информация о текущей позиции в тактах и долях.

/// Информация о текущей позиции в тактах и долях.
#[derive(Debug, Clone, Copy)]
pub struct TickInfo {
    /// Номер такта (начиная с 0).
    pub bar: u32,
    /// Доля внутри такта (0-3, где 0 – первая доля).
    pub beat: u8,
    /// Шестнадцатая внутри доли (0-3).
    pub sixteenth: u8,
    /// Абсолютная позиция в сэмплах.
    pub sample_pos: u64,
}